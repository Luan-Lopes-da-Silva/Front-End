//AND
//Only return true if both parts are true
//Example
console.log(5>3 && 5<6)
console.warn('Return true why 5 is bigger that 3 and 5 is less than 6')


//OR
//Return true if have only one expression true
//Example
console.log(5<3 || 5>4)
console.warn('Return true only why 5 is bigger that 4')

//NOT
//Invert the result(of true for false or false for true)
console.log(!true)
