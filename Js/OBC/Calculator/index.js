let numberOne = window.prompt('Insert the first number')
let numberTwo = window.prompt('Insert the second number')

alert('The sum of the two numbers is ' + (parseFloat(numberOne) + parseFloat(numberTwo)))
alert('The subtraction of the two numbers is ' + (parseFloat(numberOne) - parseFloat(numberTwo)))
alert('The division of the two numbers is ' + (parseFloat(numberOne) / parseFloat(numberTwo)))
alert('The multiplication of the two numbers is ' + (parseFloat(numberOne) * parseFloat(numberTwo)))