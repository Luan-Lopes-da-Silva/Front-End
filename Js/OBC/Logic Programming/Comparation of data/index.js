//Operators

// == 
//Verify only value is equal
//Example
console.log(5=='5')
console.warn('Return true for why only verify the value')
//Return true for why only verify the value

// ===
//Verify value and type of data one more restrict
//Example
console.log(5==='5')
console.warn('Return false for why now he verify value and type of data')
//Return false for why now he verify value and type of data



// !=
//Verify only if value is different of other, this type of verification is weak for why don´t verify type of data
//Example
console.log(5!='5')
console.warn('Return false for why he don´t verify type of data')

// !==
//Verify value and type of data, this type of verification is most used
console.log(5!=='5')
console.warn('Return true for why now he verify value and type of data')

// >
//Verify if value is bigger another one
console.log(5>3)

// <
// Verify if value is less than another one
console.log(3<5)

// >=
//Verify if value is bigger another one or equal another one.
console.log(5>=4)
console.warn('Return true why 5 is bigger that 4')


//<=
//Verify if value is less than another one or equal another one.
//Example
console.log(5<=7)
console.warn('Return true why 5 is less that 7')